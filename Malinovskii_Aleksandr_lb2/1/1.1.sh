#!/bin/bash

# Запуск нескольких утилит в фоновом режиме
echo "Запуск утилит в фоновом режиме..."
cat *.c > myprog & 
echo "Задание 1 (cat *.c > myprog) запущено в фоновом режиме с PID $!"
lpr myprog &
echo "Задание 2 (lpr myprog) запущено в фоновом режиме с PID $!"
lpr intro &
echo "Задание 3 (lpr intro) запущено в фоновом режиме с PID $!"

# Анализ списка заданий с помощью команды jobs
echo -e "\nАнализ списка заданий:"
jobs 

# Уведомление о завершении одного из заданий
echo -e "\nОжидание завершения одного из заданий..."
wait %1
echo "Задание 1 завершено."

# Возвращение невыполненных заданий в приоритетный режим
echo -e "\nВозвращение невыполненных заданий в приоритетный режим..."
fg %2
echo "Задание 2 возвращено в приоритетный режим."

# Отмена одного из невыполненных заданий
echo -e "\nОтмена одного из невыполненных заданий..."
kill %3
echo "Задание 3 отменено."

# Итоговый анализ списка заданий
echo -e "\nИтоговый анализ списка заданий:"
jobs 

echo -e "\nСкрипт завершен."