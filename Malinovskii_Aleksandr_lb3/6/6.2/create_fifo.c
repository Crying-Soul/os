/******************************************************************************
 * 
 *  Цель:
 *      Создать именованный канал (FIFO) с заданными правами доступа с помощью
 *      функции mkfifo.
 *
 *  Описание:
 *      Программа пытается создать именованный канал с именем "prog_pipe" и
 *      правами доступа 0666 (чтение и запись для всех). Если создание
 *      проходит успешно, выводится сообщение об успехе. Если происходит
 *      ошибка, выводится сообщение об ошибке.
 *
 *  Основные компоненты:
 *      - `mkfifo()`: Функция для создания именованного канала.
 *      - `perror()`: Функция для вывода сообщения об ошибке.
 *      - `printf()`: Функция для вывода сообщения об успехе.
 *
 *  Схема работы:
 *      1. Вызывается функция `mkfifo()` для создания именованного канала с
 *         именем "prog_pipe" и правами доступа 0666.
 *      2. Если `mkfifo()` возвращает -1, значит, произошла ошибка. В этом
 *         случае вызывается функция `perror()` для вывода сообщения об
 *         ошибке, и программа завершается с кодом 1.
 *      3. Если `mkfifo()` возвращает 0, значит, именованный канал успешно
 *         создан. В этом случае выводится сообщение об успехе, и программа
 *         завершается с кодом 0.
 *
 *  Примечания:
 *      - Именованные каналы (FIFO) используются для обмена данными между
 *        процессами, даже если они не имеют общего предка.
 *      - Права доступа 0666 позволяют чтение и запись для всех пользователей,
 *        но они могут быть ограничены umask.
 *      - Если именованный канал с таким именем уже существует, `mkfifo()`
 *        вернет ошибку.
 *
 ******************************************************************************/

#include <stdio.h>
#include <sys/stat.h>

int main() {
    if (mkfifo("prog_pipe", 0666) == -1) {
        perror("Ошибка создания FIFO");
        return 1;
    }
    printf("FIFO 'prog_pipe' успешно создан программно\n");
    return 0;
}