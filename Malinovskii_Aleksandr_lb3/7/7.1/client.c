/******************************************************************************
 * 
 *  Цель:
 *      Продемонстрировать создание простого TCP клиента, который подключается
 *      к серверу, отправляет сообщение и получает ответ.
 *
 *  Описание:
 *      Программа создает сокет, устанавливает соединение с сервером по
 *      указанному IP-адресу и порту, отправляет сообщение на сервер, затем
 *      принимает ответ и выводит его на экран.
 *
 *  Основные компоненты:
 *      - `socket()`:  Создает сокет.
 *      - `connect()`: Устанавливает соединение с сервером.
 *      - `send()`: Отправляет данные на сервер.
 *      - `read()`: Читает данные с сервера.
 *      - `close()`: Закрывает сокет.
 *      - `struct sockaddr_in`: Структура, содержащая информацию об адресе
 *        сервера (семейство протоколов, IP-адрес, порт).
 *      - `inet_pton()`: Преобразует IP-адрес из текстового представления в
 *        двоичное.
 *
 *  Схема работы:
 *      1. Создается сокет с использованием `socket()`.
 *      2. Заполняется структура `sockaddr_in` информацией об адресе сервера.
 *      3. Вызывается функция `connect()` для установки соединения с сервером.
 *      4. Отправляется сообщение на сервер с использованием `send()`.
 *      5. Принимается ответ от сервера с использованием `read()`.
 *      6. Выводится полученный ответ на экран.
 *      7. Закрывается сокет с использованием `close()`.
 *
 *  Примечания:
 *      - Программа предполагает, что сервер уже запущен и прослушивает
 *        указанный порт.
 *      - IP-адрес сервера жестко закодирован как "127.0.0.1" (localhost).
 *      - Порт сервера задан константой PORT (12345).
 *      - Размер буфера для приема данных ограничен BUFFER_SIZE (1024 байта).
 *      - В программе не предусмотрена обработка ошибок при разрыве соединения
 *        или других сетевых проблемах.
 *      - Для компиляции необходимы заголовочные файлы `sys/socket.h`,
 *        `netinet/in.h` и `arpa/inet.h`.
 *
 ******************************************************************************/

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <sys/socket.h>
#include <netinet/in.h>
#include <arpa/inet.h>

#define PORT 12345
#define BUFFER_SIZE 1024

int main() {
    int sock = 0;
    struct sockaddr_in serv_addr;
    char buffer[BUFFER_SIZE] = {0};
    
    // Создание сокета
    if ((sock = socket(AF_INET, SOCK_STREAM, 0)) < 0) {
        printf("\n Socket creation error \n");
        return -1;
    }
    
    serv_addr.sin_family = AF_INET;
    serv_addr.sin_port = htons(PORT);
    
    // Преобразование IP-адреса
    if (inet_pton(AF_INET, "127.0.0.1", &serv_addr.sin_addr) <= 0) {
        printf("\nInvalid address/ Address not supported \n");
        return -1;
    }
    
    // Подключение к серверу
    if (connect(sock, (struct sockaddr *)&serv_addr, sizeof(serv_addr)) < 0) {
        printf("\nConnection Failed \n");
        return -1;
    }
    
    // Отправка сообщения серверу
    char *message = "Привет, сервер!";
    send(sock, message, strlen(message), 0);
    printf("Сообщение отправлено серверу\n");
    
    // Чтение ответа от сервера
    read(sock, buffer, BUFFER_SIZE);
    printf("Ответ сервера: %s\n", buffer);
    
    close(sock);
    return 0;
}